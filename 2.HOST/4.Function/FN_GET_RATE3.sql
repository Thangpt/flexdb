CREATE OR REPLACE FUNCTION FN_GET_RATE3(PV_RATE2 IN number, PV_RATE3 IN NUMBER)
    RETURN NUMBER
    IS
    V_RESULT NUMBER(20,4);
    V_RATE   NUMBER(20,4);

BEGIN
    V_RATE:= PV_RATE2 * 1.5;
    IF PV_RATE3 = 0 THEN 
      V_RESULT := V_RATE;
    ELSE 
      IF PV_RATE3 != V_RATE THEN
        V_RESULT := PV_RATE3;
      ELSE 
        V_RESULT:= V_RATE;
      END IF;
    END IF;
RETURN V_RESULT;
EXCEPTION
   WHEN OTHERS THEN
    RETURN 0;
END;
/
