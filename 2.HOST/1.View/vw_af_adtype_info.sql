    AFTYPE.adtype||': '||ad.typename DISPLAY, AFTYPE.adtype||': '||ad.typename EN_DISPLAY, AFTYPE.adtype||': '||ad.typename DESCRIPTION,
AFTYPE.ACTYPE AFTYP, AFTYPE.adtype ADTYPEMAP, TO_NUMBER(SYS1.VARVALUE) VATRATE,  NVL( AD.advrate,TO_NUMBER(SYS2.VARVALUE)) AINTRATE,
0 AMINBAL, TO_NUMBER(SYS4.VARVALUE) AFEEBANK,TO_NUMBER(SYS5.VARVALUE) AMINFEEBANK, 0 AMINFEE,100000000000 AMAXFEE
FROM AFTYPE, SYSVAR SYS1, SYSVAR SYS2, SYSVAR SYS3, SYSVAR SYS4, SYSVAR SYS5,ADTYPE AD
WHERE SYS1.VARNAME = 'ADVVATDUTY' AND SYS1.GRNAME = 'SYSTEM'
      AND SYS2.VARNAME='AINTRATE' AND SYS2.GRNAME='SYSTEM'
      AND SYS3.VARNAME='AMINBAL' AND SYS3.GRNAME='SYSTEM'
      AND SYS4.VARNAME='AFEEBANK' AND SYS4.GRNAME='SYSTEM'
      AND SYS5.VARNAME='AMINFEEBANK' AND SYS5.GRNAME='SYSTEM'
      AND AFTYPE.ADTYPE = AD.ACTYPE
