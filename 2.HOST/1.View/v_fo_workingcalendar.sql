CREATE OR REPLACE FORCE VIEW V_FO_WORKINGCALENDAR AS
SELECT to_date(VARVALUE,'DD/MM/RRRR') TODAYDATE ,
 GETDUEDATE(BUSDATE=>to_date(VARVALUE,'DD/MM/RRRR'), CLEARCD=>'B', TRADEPLACE=>'000', CLEARDAY=>1) T1DATE,
 GETDUEDATE(BUSDATE=>to_date(VARVALUE,'DD/MM/RRRR'), CLEARCD=>'B', TRADEPLACE=>'000', CLEARDAY=>2) T2DATE,
 GETDUEDATE(BUSDATE=>to_date(VARVALUE,'DD/MM/RRRR'), CLEARCD=>'B', TRADEPLACE=>'000', CLEARDAY=>3) T3DATE
FROM   sysvar WHERE varname ='CURRDATE';

