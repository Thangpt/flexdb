CREATE OR REPLACE FORCE VIEW VW_CUSTODYCD_SUBACCOUNT AS
SELECT CF.CUSTODYCD FILTERCD, AF.ACCTNO VALUE, AF.ACCTNO VALUECD, AF.ACCTNO || ': ' || AFT.TYPENAME DISPLAY,
AF.ACCTNO || ': ' || AFT.TYPENAME EN_DISPLAY, CF.FULLNAME DESCRIPTION,AF.GROUPLEADER, AF.CAREBY, AF.STATUS
FROM CFMAST CF, AFMAST AF, AFTYPE AFT
WHERE CF.CUSTID=AF.CUSTID AND AF.ACTYPE=AFT.ACTYPE;
