CREATE OR REPLACE FORCE VIEW V_AFMAST AS
SELECT ACTYPE, CUSTID, ACCTNO, AFTYPE , A1.CDCONTENT TRADEFLOOR, A2.CDCONTENT TRADETELEPHONE, 
    A3.CDCONTENT TRADEONLINE, PIN, A4.CDCONTENT LANGUAGE, TRADEPHONE, A5.CDCONTENT ALLOWDEBIT, 
    BANKACCTNO, A6.CDCONTENT BANKNAME, SWIFTCODE, A7.CDCONTENT RECEIVEVIA, EMAIL, ADDRESS, FAX,
    CIACCTNO, IFRULECD, LASTDATE, A8.CDCONTENT STATUS, MARGINLINE, TRADELINE, ADVANCELINE, 
    REPOLINE, DEPOSITLINE, BRATIO, A9.CDCONTENT TERMOFUSE, DESCRIPTION  
FROM AFMAST, ALLCODE A1, ALLCODE A2, ALLCODE A3, ALLCODE A4, ALLCODE A5, ALLCODE A6,
    ALLCODE A7, ALLCODE A8, ALLCODE A9
WHERE A1.CDTYPE = 'SY' AND A1.CDNAME = 'YESNO' AND A1.CDVAL= TRADEFLOOR 
    AND A2.CDTYPE = 'SY' AND A2.CDNAME = 'YESNO' AND A2.CDVAL= TRADETELEPHONE 
    AND A3.CDTYPE = 'SY' AND A3.CDNAME = 'YESNO' AND A3.CDVAL= TRADEONLINE 
    AND A4.CDTYPE = 'CF' AND A4.CDNAME = 'LANGUAGE' AND A4.CDVAL = LANGUAGE 
    AND A5.CDTYPE = 'SY' AND A5.CDNAME = 'YESNO' AND A5.CDVAL = ALLOWDEBIT 
    AND A6.CDTYPE = 'CF' AND A6.CDNAME = 'BANKNAME' AND A6.CDVAL = BANKNAME 
    AND A7.CDTYPE = 'CF' AND A7.CDNAME = 'RECEIVEVIA' AND A7.CDVAL = RECEIVEVIA 
    AND A8.CDTYPE = 'CF' AND A8.CDNAME = 'STATUS' AND A8.CDVAL = STATUS 
    AND A9.CDTYPE = 'CF' AND A9.CDNAME = 'TERMOFUSE' AND A9.CDVAL = TERMOFUSE;

