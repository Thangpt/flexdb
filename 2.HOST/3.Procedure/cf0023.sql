      V_STRCAREBY := CAREBY;
   ELSE
      V_STRCAREBY := '%%';
   END IF;

   -- END OF GETTING REPORT'S PARAMETERS

   -- GET REPORT'S DATA
   IF (V_STROPTION <> 'A') AND (BRID <> 'ALL')
   THEN
      OPEN PV_REFCURSOR
       FOR
