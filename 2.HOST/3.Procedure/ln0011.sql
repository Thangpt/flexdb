CREATE OR REPLACE PROCEDURE ln0011 (
   PV_REFCURSOR   IN OUT   PKG_REPORT.REF_CURSOR,
   OPT            IN       VARCHAR2,
   BRID           IN       VARCHAR2,
   F_DATE         IN       VARCHAR2,
   T_DATE         IN       VARCHAR2,
   CUSTODYCD      IN       VARCHAR2,
   CIACCTNO       IN       VARCHAR2,
   BRGID          IN       VARCHAR2
   )
IS
--
-- PURPOSE: BRIEFLY EXPLAIN THE FUNCTIONALITY OF THE PROCEDURE
-- BANG KE PHAT VAY BAO LANH.
-- MODIFICATION HISTORY
-- PERSON      DATE    COMMENTS
-- DUNGNH   17-MAY-10  CREATED
-- HUONG.TTD 27-OCT-10 UPDATED (them ma giao dich)
-- ---------   ------  -------------------------------------------

   V_STROPTION    VARCHAR2 (5);            -- A: ALL; B: BRANCH; S: SUB-BRANCH
   V_STRBRID      VARCHAR2 (4);            -- USED WHEN V_NUMOPTION > 0

   V_STRCUSTODYCD VARCHAR2 (20);
   V_STRCIACCTNO  VARCHAR2 (20);
   V_STRBRGID  VARCHAR2 (10);


   V_FROMDATE     DATE;
   V_TODATE       DATE;

BEGIN
   V_STROPTION := OPT;

   IF V_STROPTION = 'A' THEN     -- TOAN HE THONG
      V_STRBRID := '%';
   ELSIF V_STROPTION = 'B' THEN
      V_STRBRID := SUBSTR(BRID,1,2) || '__' ;
   ELSE
      V_STRBRID := BRID;
   END IF;

   IF(CUSTODYCD <> 'ALL') THEN
     V_STRCUSTODYCD := CUSTODYCD;
   ELSE
     V_STRCUSTODYCD := '%';
   END IF;

   IF(CIACCTNO <> 'ALL') THEN
     V_STRCIACCTNO := CIACCTNO;
   ELSE
     V_STRCIACCTNO := '%';
   END IF;

   IF(BRGID <> 'ALL') THEN
     V_STRBRGID := BRGID;
   ELSE
     V_STRBRGID := '%';
   END IF;


   V_FROMDATE  :=    TO_DATE(F_DATE,'DD/MM/YYYY');
   V_TODATE    :=    TO_DATE(T_DATE,'DD/MM/YYYY');

OPEN PV_REFCURSOR
FOR
SELECT LNTR.TXDATE,LNTR.TLTXCD, LNTR.TXNUM, CF.CUSTODYCD, AF.ACCTNO, CF.FULLNAME,
    SUM(CASE WHEN APP.FIELD IN ('PRINPAID','OPRINPAID') THEN LNTR.NAMT ELSE 0 END) PRINPAID,
    SUM(CASE WHEN APP.FIELD IN ('INTPAID','OINTPAID') THEN LNTR.NAMT ELSE 0 END) INTPAID,
    TLPR.TLNAME, BR.BRNAME
FROM VW_LNTRAN_ALL LNTR, APPTX APP, VW_LNMAST_ALL LN,
    AFMAST AF, CFMAST CF, TLPROFILES TLPR, BRGRP BR
WHERE LNTR.TXCD = APP.TXCD
    AND LNTR.ACCTNO = LN.ACCTNO
    AND LN.FTYPE = 'AF'
    AND APP.FIELD IN ('PRINPAID','OPRINPAID','INTPAID','OINTPAID')
    AND APP.APPTYPE LIKE 'LN'
    AND LNTR.NAMT > 1
    AND LN.TRFACCTNO = AF.ACCTNO
    AND AF.CUSTID = CF.CUSTID
    AND CF.TLID = TLPR.TLID(+)
    AND TLPR.BRID = BR.BRID(+)
    AND LNTR.TXDATE BETWEEN V_FROMDATE AND V_TODATE
    AND CF.CUSTODYCD LIKE V_STRCUSTODYCD
    AND AF.ACCTNO LIKE V_STRCIACCTNO
    AND nvl(BR.BRID,'_') LIKE V_STRBRGID
    AND CF.CUSTODYCD NOT LIKE '017P000002'
    AND CF.CUSTODYCD LIKE '017C%'
GROUP BY LNTR.TXDATE,LNTR.TLTXCD, LNTR.TXNUM, CF.CUSTODYCD, AF.ACCTNO, CF.FULLNAME,
    TLPR.TLNAME, BR.BRNAME
ORDER BY LNTR.TXDATE, CF.CUSTODYCD

;

EXCEPTION
   WHEN OTHERS
   THEN
      RETURN;
END;                                                              -- PROCEDURE
