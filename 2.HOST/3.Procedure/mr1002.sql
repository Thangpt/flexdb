CREATE OR REPLACE PROCEDURE MR1002 (
   PV_REFCURSOR   IN OUT   PKG_REPORT.REF_CURSOR,
   OPT            IN       VARCHAR2,
   BRID           IN       VARCHAR2,
   PV_VOUCHER     IN       VARCHAR2
       )
IS

-- RP NAME : Bang ke tat toan MR
-- PERSON : PhucPP
-- DATE : 16/03/2012
-- COMMENTS : Create New
-- ---------   ------  -------------------------------------------
   V_SYMBOL  VARCHAR2 (15);
   V_CUSTODYCD VARCHAR2 (15);
BEGIN
-- GET REPORT'S PARAMETERS



-- GET REPORT'S DATA
 OPEN PV_REFCURSOR
 FOR

 SELECT MST.OBJNAME, BRID, FN_CRB_GETVOUCHERNO(LG.TRFCODE, LG.TXDATE, LG.VERSION) VOUCHERNO,  MST.TXDATE, MST.OBJKEY, GREATEST( datediff('day' , TO_DATE(RF.RLSDATE_R,'DD/MM/YYYY'), TO_DATE(TO_CHAR(MST.TXDATE),'DD/MM/YYYY')), RF.MINTERM_R) SO_NGAY,
      MST.AFACCTNO, MST.BANKCODE, MST.BANKACCT, MST.STATUS, MST.TXAMT, RF.*
    FROM CRBTXREQ MST, CRBTRFLOG LG, CRBTRFLOGDTL LGDTL,
      (SELECT *
      FROM   (SELECT DTL.REQID, DTL.FLDNAME, NVL(DTL.CVAL,DTL.NVAL) REFVAL
              FROM   CRBTXREQ MST, CRBTXREQDTL DTL WHERE  MST.REQID=DTL.REQID )
      PIVOT  (MAX(REFVAL) AS R FOR (FLDNAME) IN
      ('FULLNAME' as FULLNAME, 'AFACCTNO' as AFACCTNO, 'RLSDATE' as RLSDATE, 'MINTERM' as MINTERM, 'RLSAMT' as RLSAMT,
      'CURAMT' as CURAMT, 'DAYS' as DAYS, 'RATE2' as RATE2, 'CURINT' as CURINT,
      'CURFEE' as CURFEE,'BANKPAY' as BANKPAY,'TOTALPAY' as TOTALPAY, 'TXAMT' as TXAMT, 'AMTLEFT' as AMTLEFT,
      'DESC' as NOTES))
      ORDER BY REQID) RF
    WHERE MST.REQID=RF.REQID
      AND LG.VERSION = LGDTL.VERSION
      AND LG.TXDATE = LGDTL.TXDATE
      AND LG.TRFCODE = LGDTL.TRFCODE
      AND LGDTL.REFREQID = MST.REQID
      AND LG.TRFCODE = 'LOANPAYMENT'
      AND lpad(LGDTL.VERSION, 3,'0') = SUBSTR(PV_VOUCHER,15,3)
      AND LGDTL.TXDATE = TO_DATE((SUBSTR(PV_VOUCHER,9,2) || '/' || SUBSTR(PV_VOUCHER,11,2)  || '/' || '20' || SUBSTR(PV_VOUCHER,13,2)),'DD/MM/YYYY')
      ;

EXCEPTION
   WHEN OTHERS
   THEN
      RETURN;
END;
