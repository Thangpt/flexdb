CREATE OR REPLACE PROCEDURE cf0099 (
   PV_REFCURSOR   IN OUT   PKG_REPORT.REF_CURSOR,
   OPT            IN       VARCHAR2,
   BRID           IN       VARCHAR2,
   CUSTODYCD      IN       VARCHAR2,
   AFACCTNO       IN       VARCHAR2,
   SYMBOL         IN       VARCHAR2
  )
IS
--
-- PURPOSE: BRIEFLY EXPLAIN THE FUNCTIONALITY OF THE PROCEDURE
-- DANH MUC DAU TU - CLOSE POSITION
-- MODIFICATION HISTORY
-- PERSON      DATE    COMMENTS
-- DUNGNH   18-AUG-10  CREATED
-- ---------   ------  -------------------------------------------

   CUR             PKG_REPORT.REF_CURSOR;
   V_STROPTION    VARCHAR2 (5);            -- A: ALL; B: BRANCH; S: SUB-BRANCH
   V_STRBRID      VARCHAR2 (4);                  -- USED WHEN V_NUMOPTION > 0

   V_STRAFACCTNO  VARCHAR2(20);
   V_STRUTODYCD   VARCHAR2(20);
   V_STRSYMBOL    VARCHAR2(10);

   V_VATRATE      NUMBER(20,4)  ;
   V_DEFFEERATE   NUMBER(20,4)  ;

BEGIN
   V_STROPTION := OPT;

   IF (V_STROPTION <> 'A') AND (BRID <> 'ALL')
   THEN
      V_STRBRID := BRID;
   ELSE
      V_STRBRID := '%%';
   END IF;

   -- GET REPORT'S PARAMETERS

   IF (SYMBOL <> 'ALL' )
   THEN
      V_STRSYMBOL := SYMBOL;
   ELSE
      V_STRSYMBOL := '%';
   END IF;

   V_STRAFACCTNO := AFACCTNO;
   V_STRUTODYCD  := CUSTODYCD;




   --TI LE PHI GIAO DICH CAO NHAT TINH THEO SO TIEU KHOAN

    SELECT SUM(nvl(RATE,0)) INTO V_DEFFEERATE
    FROM
    (
        SELECT ICRATE RATE
        FROM ICCFTYPEDEF ICC, AFMAST AF
        WHERE EVENTCODE = 'DTRADEFEE'
            AND ICC.ACTYPE = AF.ACTYPE
            AND AF.ACCTNO = V_STRAFACCTNO
        UNION ALL
        SELECT MAX(DELTA) RATE
        FROM ICCFTIER ICC, AFMAST AF
        WHERE EVENTCODE = 'DTRADEFEE'
            AND ICC.ACTYPE = AF.ACTYPE
            AND AF.ACCTNO = V_STRAFACCTNO
    );
    --TI LE THUE THU NHAP CA NHAN.


  SELECT
    decode(aft.VAT,'Y', nvl((SELECT icrate FROM ICCFTYPEDEF WHERE modcode ='CF' AND eventcode='CFSELLVAT' and Actype=AF.actype GROUP BY icrate),0.1),0)
    INTO V_VATRATE
    FROM  AFMAST AF , aftype aft
    WHERE AF.actype = AFT.actype
    AND AF.ACCTNO = V_STRAFACCTNO;

   /* SELECT max(nvl(ICRATE,0)) INTO V_VATRATE
    FROM ICCFTYPEDEF ICC, AFMAST AF
    WHERE  EVENTCODE = 'CFSELLVAT'
        AND ICC.ACTYPE = AF.ACTYPE
        AND AF.ACCTNO = V_STRAFACCTNO;*/
   -- END OF GETTING REPORT'S PARAMETERS

   -- GET REPORT'S DATA

OPEN PV_REFCURSOR
FOR
    SELECT SE.CUSTODYCD, SE.AFACCTNO, SE.SYMBOL, SE.TOTALSELLQTTY,
        SE.AVGBUYCOSTPRICE, SE.AVGSELLPRICE, SE.DEALINTPAID , V_DEFFEERATE V_DEFFEERATE , V_VATRATE V_VATRATE
    FROM VW_STRADE_CLOSED_POSITION SE
    WHERE SYMBOL LIKE V_STRSYMBOL
        AND CUSTODYCD LIKE V_STRUTODYCD
        AND AFACCTNO LIKE V_STRAFACCTNO
;

EXCEPTION
   WHEN OTHERS
   THEN
      RETURN;
END;                                                              -- PROCEDURE
