CREATE OR REPLACE PROCEDURE PR_NOTIFYHOSTATUS2FO(pv_hostatus IN VARCHAR2)
IS
   pv_ref    pkg_report.ref_cursor;
BEGIN
   OPEN pv_ref FOR
   SELECT 'R' MSGTYPE, 'SYS' DATATYPE, DECODE(pv_hostatus, '1', 'OPEN', 'CLOSE') REFID FROM dual;
   txpks_NOTIFY.PR_NOTIFYEVENT2FO(pv_ref, 'PUSH2FO');
EXCEPTION WHEN OTHERS THEN
   RETURN;
END;
/
