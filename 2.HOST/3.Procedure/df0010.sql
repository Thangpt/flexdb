CREATE OR REPLACE PROCEDURE df0010 (
   PV_REFCURSOR   IN OUT   PKG_REPORT.REF_CURSOR,
   OPT            IN       VARCHAR2,
   BRID           IN       VARCHAR2,
   F_DATE         IN       VARCHAR2,
   T_DATE         IN       VARCHAR2,
   PV_ACTYPE      IN       VARCHAR2,
   I_CUSTODYCD    IN       VARCHAR2,
   PV_AFACCTNO    IN       VARCHAR2,
   GRCAREBY       IN       VARCHAR2

   )
IS
--
-- PURPOSE: BRIEFLY EXPLAIN THE FUNCTIONALITY OF THE PROCEDURE
-- BAO CAO SU DU TK VAY BAO LANH TINH THEO NGAY PHAT SINH
-- MODIFICATION HISTORY
-- PERSON      DATE    COMMENTS
-- DUNGNH   17-MAY-10  CREATED
-- ---------   ------  -------------------------------------------

   V_STROPTION    VARCHAR2 (5);            -- A: ALL; B: BRANCH; S: SUB-BRANCH
   V_STRBRID      VARCHAR2 (4);            -- USED WHEN V_NUMOPTION > 0



   V_FROMDATE           DATE;
   V_TODATE             DATE;
   V_STRI_CUSTODYCD     VARCHAR2(20);
   V_STRI_ACTYPE        VARCHAR2(20);
   V_STRI_AFACCTNO      VARCHAR2(20);
   V_CAREBY             VARCHAR2 (20);


BEGIN
   V_STROPTION := OPT;

   IF V_STROPTION = 'A' THEN     -- TOAN HE THONG
      V_STRBRID := '%';
   ELSIF V_STROPTION = 'B' THEN
      V_STRBRID := SUBSTR(BRID,1,2) || '__' ;
   ELSE
      V_STRBRID := BRID;
   END IF;


   V_FROMDATE   :=  to_date(F_DATE,'DD/MM/YYYY');
   V_TODATE     :=  to_date(T_DATE,'DD/MM/YYYY');


   IF(I_CUSTODYCD <> 'ALL') THEN
      V_STRI_CUSTODYCD := I_CUSTODYCD;
   ELSE
      V_STRI_CUSTODYCD := '%';
   END IF;


    IF(PV_ACTYPE <> 'ALL') THEN
      V_STRI_ACTYPE := PV_ACTYPE;
   ELSE
      V_STRI_ACTYPE := '%';
   END IF;

    IF(PV_AFACCTNO <> 'ALL') THEN
      V_STRI_AFACCTNO := PV_AFACCTNO;
      ELSE
      V_STRI_AFACCTNO := '%';
   END IF;

   IF (GRCAREBY <> 'ALL')
  THEN
     V_CAREBY := GRCAREBY;
  ELSE
      V_CAREBY := '%';
   END IF;

----- GET REPORT DATA:
OPEN PV_REFCURSOR
FOR

    SELECT SCHD.RLSDATE, CF.CUSTODYCD, CI.ACCTNO AFACCTNO, CF.FULLNAME, SCHD.ACCTNO, AFT.ACTYPE, SY.VARVALUE BUSDATE,
        (SCHD.NML+SCHD.OVD+SCHD.PAID)  PRINPAID, SCHD.PAID,
        ROUND((SCHD.INTNMLACR + SCHD.INTDUE + SCHD.INTOVD + SCHD.INTOVDPRIN + SCHD.INTPAID),0) INTLN,
        (SCHD.NML+SCHD.OVD) DEBTLEFT , CASE WHEN (SCHD.NML+SCHD.OVD) <> 0 THEN (TO_DATE(SY.VARVALUE,'DD-MM-YYYY') -  TO_DATE(SCHD.RLSDATE,'DD-MM-YYYY') ) ELSE (TO_DATE(SCHD.OVDACRDATE,'DD-MM-YYYY') -  TO_DATE(SCHD.RLSDATE,'DD-MM-YYYY') )  END DEALDAYS
    FROM (SELECT * FROM LNSCHD UNION ALL SELECT * FROM LNSCHDHIST) SCHD, LNMAST MST, CFMAST CF,
        CIMAST CI, AFMAST AF, AFTYPE AFT, SYSVAR SY
    WHERE MST.ACCTNO = SCHD.ACCTNO
        AND AF.CUSTID = CF.CUSTID
        AND AF.ACTYPE = AFT.ACTYPE
        AND AF.ACCTNO = CI.ACCTNO
        AND SCHD.REFTYPE IN ('P','GP')
        AND CI.ACCTNO = MST.TRFACCTNO
        AND CF.CUSTID = CI.CUSTID
        AND MST.FTYPE = 'AF'
        AND SY.VARNAME = 'CURRDATE'
        AND CF.CUSTODYCD LIKE V_STRI_CUSTODYCD
        AND AF.ACCTNO LIKE V_STRI_AFACCTNO
        AND AF.ACTYPE LIKE V_STRI_ACTYPE
        AND AF.CAREBY LIKE V_CAREBY
        AND SCHD.RLSDATE BETWEEN V_FROMDATE AND V_TODATE

;
EXCEPTION
   WHEN OTHERS
   THEN
      RETURN;
END;                                                              -- PROCEDURE
/

