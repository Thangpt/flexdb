FUNCTION fn_txAftAppUpdate(p_txmsg in tx.msg_rectype,p_err_code out varchar2)
RETURN NUMBER
IS
    l_total_trft0amt number(20,0);
    l_trft0amt number(20,0);
BEGIN
    plog.setbeginsection (pkgctx, 'fn_txAftAppUpdate');
    plog.debug (pkgctx, '<<BEGIN OF fn_txAftAppUpdate');
   /***************************************************************************************************
    ** PUT YOUR SPECIFIC AFTER PROCESS HERE. DO NOT COMMIT/ROLLBACK HERE, THE SYSTEM WILL DO IT
    ***************************************************************************************************/
    IF txpks_batch.fn_SettlementOrder(p_txmsg,p_err_code) <> systemnums.C_SUCCESS THEN
       RETURN errnums.C_BIZ_RULE_INVALID;
    END IF;

    -- Cap nhat bao lanh duoc dung cho lenh thanh toan.
    select nvl(sum(sts.trft0amt),0)
        into l_total_trft0amt
    from stschd sts
    where sts.afacctno = p_txmsg.txfields(c_ciacctno).value
    and sts.duetype = 'SM' and deltd <> 'Y' and trfbuysts <> 'Y' and trfbuyrate > 0 and trfbuyext > 0;

    select least(af.advanceline - l_total_trft0amt, nvl(sts.amt,0) * nvl(sts.trfbuyrate,0) / 100 + (nvl(sts.feeacr,0) - nvl(sts.feeamt,0)))
        into l_trft0amt
    from afmast af,
        (select sts.afacctno, sts.trfbuyrate, od.feeacr, od.feeamt , sts.amt
        from stschd sts, odmast od
        where sts.orgorderid = od.orderid and sts.txdate = p_txmsg.txdate
        and duetype = 'SM' and sts.deltd <> 'Y' and autoid = p_txmsg.txfields(c_autoid).value) sts
    where af.acctno = sts.afacctno(+) and af.acctno = p_txmsg.txfields(c_ciacctno).value;

    update stschd
    set trft0amt = l_trft0amt
    where autoid = p_txmsg.txfields(c_autoid).value and txdate = p_txmsg.txdate;

    plog.debug (pkgctx, '<<END OF fn_txAftAppUpdate');
    plog.setendsection (pkgctx, 'fn_txAftAppUpdate');
    RETURN systemnums.C_SUCCESS;
EXCEPTION
WHEN OTHERS
   THEN
      p_err_code := errnums.C_SYSTEM_ERROR;
      plog.error (pkgctx, SQLERRM);
       plog.setendsection (pkgctx, 'fn_txAftAppUpdate');
      RAISE errnums.E_SYSTEM_ERROR;
END fn_txAftAppUpdate;

