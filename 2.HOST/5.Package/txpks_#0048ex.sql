FUNCTION fn_txAftAppUpdate(p_txmsg in tx.msg_rectype,p_err_code out varchar2)
RETURN NUMBER
IS
    l_autoid        NUMBER;
	l_custid 		varchar2(20);
BEGIN
    plog.setbeginsection (pkgctx, 'fn_txAftAppUpdate');
    plog.debug (pkgctx, '<<BEGIN OF fn_txAftAppUpdate');
   /***************************************************************************************************
    ** PUT YOUR SPECIFIC AFTER PROCESS HERE. DO NOT COMMIT/ROLLBACK HERE, THE SYSTEM WILL DO IT
    ***************************************************************************************************/

    l_autoid := seq_cf_branch_log.NEXTVAL;
	select custid into l_custid from cfmast where  custodycd = p_txmsg.txfields('88').value;
    IF p_txmsg.deltd = 'N' THEN
        UPDATE cfmast SET brid = p_txmsg.txfields('02').value WHERE custodycd = p_txmsg.txfields('88').value;
		update afmast set brid = p_txmsg.txfields('02').value WHERE custid = l_custid ; -- 1.7.2.4
		
        INSERT INTO cf_branch_log
        VALUES (l_autoid, p_txmsg.txfields('88').value, substr(p_txmsg.txfields('01').value,1,4), p_txmsg.txfields('02').value, p_txmsg.tlid,
                p_txmsg.offid, getcurrdate, p_txmsg.txnum, p_txmsg.txfields('30').value);
    ELSE
        UPDATE cfmast SET brid = substr(p_txmsg.txfields('01').value,1,4) WHERE custodycd = p_txmsg.txfields('88').value;
		update afmast set brid = substr(p_txmsg.txfields('01').value,1,4) WHERE custid = l_custid; --1.7.2.4
        DELETE FROM cf_branch_log
        WHERE custodycd = p_txmsg.txfields('88').value AND obrid = substr(p_txmsg.txfields('01').value,1,4)
            AND nbrid = p_txmsg.txfields('02').value AND txdate = getcurrdate;
    END IF;

    plog.debug (pkgctx, '<<END OF fn_txAftAppUpdate');
    plog.setendsection (pkgctx, 'fn_txAftAppUpdate');
    RETURN systemnums.C_SUCCESS;
EXCEPTION
WHEN OTHERS
   THEN
      p_err_code := errnums.C_SYSTEM_ERROR;
      plog.error (pkgctx, SQLERRM || dbms_utility.format_error_backtrace);
       plog.setendsection (pkgctx, 'fn_txAftAppUpdate');
      RAISE errnums.E_SYSTEM_ERROR;
END fn_txAftAppUpdate;

